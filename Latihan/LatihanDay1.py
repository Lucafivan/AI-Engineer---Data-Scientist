N1 = int(input("Masukkan angka pertama anda: "))
N2 = int(input("Masukkan angka kedua anda: "))

Tambah = N1 + N2
Kurang = N1 - N2
Kali = N1 * N2
Bagi = N1 / N2
Sisa = N1 % N2
Pangkat = N1 ** N2

print("Berikut adalah hasil yang anda perlukan")
print("Hasil penjumlahan dari ", N1, " dan ", N2, " adalah ", Tambah)
print("Hasil pengurangan dari ", N1, " dan ", N2, " adalah ", Kurang)
print("Hasil perkalian dari ", N1, " dan ", N2, " adalah ", Kali)
print("Hasil pembagian dari ", N1, " dan ", N2, " adalah ", Bagi)
print("Sisa pembagian dari ", N1, " dan ", N2, " adalah ", Sisa)
print("Hasil perpangkatan dari ", N1, " dan ", N2, " adalah ", Pangkat)